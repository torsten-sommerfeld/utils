package com.torstensommerfeld.utils.math;

public class MathUtil {
    public static int sqr(int value) {
        return value * value;
    }

    public static long sqr(long value) {
        return value * value;
    }

    public static double sqr(double value) {
        return value * value;
    }

    public static float sqr(float value) {
        return value * value;
    }

}
